export const MONTHS = ['Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun']
export const CATEGORIES = ['Electronics', 'Furniture', 'Software']
export const REGIONS = ['North', 'South']

export const CATEGORY_COLORS = {
  Electronics: '#6366f1',
  Furniture: '#f59e0b',
  Software: '#10b981',
}

export const REGION_COLORS = {
  North: '#3b82f6',
  South: '#f97316',
}

// Aggregate revenue by month
export const revenueByMonth = MONTHS.map(month => {
  const rows = salesData.filter(d => d.month === month)
  return {
    month,
    total: rows.reduce((s, d) => s + d.revenue_eur, 0),
    Electronics: rows.filter(d => d.product_category === 'Electronics').reduce((s, d) => s + d.revenue_eur, 0),
    Furniture: rows.filter(d => d.product_category === 'Furniture').reduce((s, d) => s + d.revenue_eur, 0),
    Software: rows.filter(d => d.product_category === 'Software').reduce((s, d) => s + d.revenue_eur, 0),
  }
})

// Aggregate revenue by category
export const revenueByCategory = CATEGORIES.map(cat => ({
  category: cat,
  revenue: salesData.filter(d => d.product_category === cat).reduce((s, d) => s + d.revenue_eur, 0),
  units: salesData.filter(d => d.product_category === cat).reduce((s, d) => s + d.units_sold, 0),
}))

// Aggregate revenue by region
export const revenueByRegion = REGIONS.map(region => ({
  region,
  revenue: salesData.filter(d => d.region === region).reduce((s, d) => s + d.revenue_eur, 0),
}))

// Revenue by month and region
export const revenueByMonthRegion = MONTHS.map(month => {
  const rows = salesData.filter(d => d.month === month)
  return {
    month,
    North: rows.filter(d => d.region === 'North').reduce((s, d) => s + d.revenue_eur, 0),
    South: rows.filter(d => d.region === 'South').reduce((s, d) => s + d.revenue_eur, 0),
  }
})

// Margin by category (revenue - cost)
export const marginByCategory = CATEGORIES.map(cat => {
  const revenue = salesData.filter(d => d.product_category === cat).reduce((s, d) => s + d.revenue_eur, 0)
  const cost = salesData.filter(d => d.product_category === cat).reduce((s, d) => s + d.cost_eur, 0)
  return {
    category: cat,
    revenue,
    cost,
    margin: revenue - cost,
    marginPct: Math.round(((revenue - cost) / revenue) * 100),
  }
})

// Total KPIs
export const totalRevenue = salesData.reduce((s, d) => s + d.revenue_eur, 0)
export const totalUnits = salesData.reduce((s, d) => s + d.units_sold, 0)
export const totalProfit = salesData.reduce((s, d) => s + (d.revenue_eur - d.cost_eur), 0)
export const overallMarginPct = Math.round((totalProfit / totalRevenue) * 100)
